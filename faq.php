                <h3>Isn't it just mobile device management (MDM)?</h3>
                <p>While MokiManage  is mobile device management at the core, it has been built to solve fundamentally different problems. Traditional MDM platforms have been built to manage devices typically owned by a single individual attaching to a corporate network. MokiManage has been built to enable solutions for devices that are designed to serve a single purpose (like a point-of-sale or kiosk).</p>

                <h3>What's the difference between MokiManage and mobile application management (MAM)?</h3>
                <p>Mobile Application Management platforms traditionally focus on simply getting applications on a device and keeping them updated, but most stop at managing apps. MokiManage also manages apps on devices (installing, removing and updating apps centrally), but also has more control over the entire device, including: wifi settings, email, passcode requirements, etc. MokiManage also takes application management a step further by enabling the central management of application settings. Traditional MAM platforms enable the one-time personalization of an app on deployment for user-specific settings. MokiManage enables the remote management of app settings during run-time. This feature is necessary when running devices as single-purpose solutions and the user wants to change the way the device operates without pushing a new version of the app. Using MokiManage, users can change app settings in a web-based console and the app with receive these new settings immediately on the device. Changes on the device itself are also reflected in the web-based console.</p>

                <h3>What is remote application settings management?</h3>
                <p>Remote Application Settings Management (ASM) is the management of app settings through a web-based console. Many single-purpose solutions require the ability to remotely adjust how the app runs without pushing an update to the app. RASM enables a user to change the way and app behaves from a web-based console. For example, MokiTouch has settings such as the home page URL and a white-list of approved sites. These settings can be adjusted through a web-based console, rather than directly on the device alone or by pushing a new version of the app. RASM enables faster, more efficient management of apps on remote devices than any other method.</p>

